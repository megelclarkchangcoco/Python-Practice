# Question Three:Index Operator
# Write a Python program that takes a list of strings as input and prints the first character of each string using index operator.
#
# Sample Input:
# ['apple', 'banana', 'cherry']
#
# Sample Output:
# First characters:
# a
# b
# c

fruits = ['apple', 'banana', 'cherry']

print(fruits[0][0])
print(fruits[1][0])
print(fruits[2][0])

